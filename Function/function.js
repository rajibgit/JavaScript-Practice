//addition
function addition(a, b){
    var result = a + b;
    document.write("Add = " + result + "<br>");
}

//subtract
function subtract(a , b){
    var result = a - b;
    document.write("Sub = " + result + "<br>");
 
}

function multiplication(a , b){
    var result = a * b;
    document.write("Mul = " + result + "<br>");
}

//calling addition function
addition(5, 5);
subtract(8, 5);
multiplication(8 , 8);

